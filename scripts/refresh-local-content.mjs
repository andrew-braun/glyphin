import { execFileSync, spawnSync } from "node:child_process";
import { mkdtempSync, rmSync, writeFileSync } from "node:fs";
import { tmpdir } from "node:os";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(scriptDir, "..");
const defaultDbUrl = "postgresql://postgres:postgres@127.0.0.1:54322/postgres";

const backupTables = [
	"auth.users",
	"auth.identities",
	"auth.sessions",
	"auth.refresh_tokens",
	"auth.one_time_tokens",
	"auth.mfa_factors",
	"auth.mfa_amr_claims",
	"auth.mfa_challenges",
	"auth.webauthn_credentials",
	"auth.webauthn_challenges",
	"auth.oauth_authorizations",
	"auth.oauth_consents",
	"learner.profiles",
	"learner.preferences",
	"learner.devices",
	"learner.course_enrollments",
	"learner.lesson_progress",
	"learner.lesson_attempts",
];

function logStep(message) {
	console.log(`\n==> ${message}`);
}

function formatCommand(command, args) {
	return [command, ...args].join(" ");
}

function run(command, args, options = {}) {
	const result = spawnSync(command, args, {
		cwd: repoRoot,
		stdio: "inherit",
		env: process.env,
		...options,
	});

	if (result.status !== 0) {
		throw new Error(`Command failed: ${formatCommand(command, args)}`);
	}
}

function capture(command, args, options = {}) {
	try {
		return execFileSync(command, args, {
			cwd: repoRoot,
			encoding: "utf8",
			env: process.env,
			maxBuffer: 16 * 1024 * 1024,
			stdio: ["ignore", "pipe", "pipe"],
			...options,
		});
	} catch (error) {
		if (error instanceof Error) {
			const stderr =
				typeof error.stderr === "string" && error.stderr.trim().length > 0
					? `\n${error.stderr.trim()}`
					: "";
			throw new Error(`Command failed: ${formatCommand(command, args)}${stderr}`, {
				cause: error,
			});
		}

		throw error;
	}
}

function parseEnvOutput(output) {
	const values = {};

	for (const rawLine of output.split(/\r?\n/u)) {
		const line = rawLine.trim();
		if (!line || line.startsWith("Stopped services:")) continue;

		const equalsIndex = line.indexOf("=");
		if (equalsIndex < 0) continue;

		const key = line.slice(0, equalsIndex).trim();
		let value = line.slice(equalsIndex + 1).trim();

		if (
			(value.startsWith('"') && value.endsWith('"')) ||
			(value.startsWith("'") && value.endsWith("'"))
		) {
			value = value.slice(1, -1);
		}

		values[key] = value;
	}

	return values;
}

function sqlLiteral(value) {
	return `'${String(value).replace(/'/g, "''")}'`;
}

function dollarQuote(value) {
	let tag = "glyphin_backup";

	while (value.includes(`$${tag}$`)) {
		tag = `${tag}_x`;
	}

	return `$${tag}$${value}$${tag}$`;
}

function getLocalDbUrl() {
	if (process.env.DB_URL) {
		return process.env.DB_URL;
	}

	const statusOutput = capture("pnpm", ["exec", "supabase", "status", "-o", "env"]);
	const statusEnv = parseEnvOutput(statusOutput);

	return statusEnv.DB_URL || defaultDbUrl;
}

function getInsertableColumns(dbUrl, table) {
	const [schema, name] = table.split(".");
	const output = capture("psql", [
		dbUrl,
		"-At",
		"-c",
		`select quote_ident(column_name)
from information_schema.columns
where table_schema = ${sqlLiteral(schema)}
	and table_name = ${sqlLiteral(name)}
	and is_generated = 'NEVER'
order by ordinal_position;`,
	]).trim();

	return output ? output.split(/\r?\n/u) : [];
}

function getTableJson(dbUrl, table) {
	return (
		capture("psql", [
			dbUrl,
			"-At",
			"-c",
			`select coalesce(jsonb_agg(to_jsonb(t)), '[]'::jsonb)::text
from ${table} t;`,
		]).trim() || "[]"
	);
}

function buildRestoreFile(dbUrl, backupDir) {
	const restoreSections = [
		"-- Local auth and learner state restore generated by scripts/refresh-local-content.mjs.",
		"begin;",
		"set local session_replication_role = replica;",
	];

	for (const table of backupTables) {
		const columns = getInsertableColumns(dbUrl, table);
		const rowJson = getTableJson(dbUrl, table);

		restoreSections.push(`\n-- ${table}`);
		restoreSections.push(
			`insert into ${table} (${columns.join(", ")})
select ${columns.join(", ")}
from jsonb_populate_recordset(null::${table}, ${dollarQuote(rowJson)}::jsonb);`,
		);
	}

	const restoreFile = resolve(backupDir, "restore-user-state.sql");
	writeFileSync(restoreFile, `${restoreSections.join("\n")}\ncommit;\n`, "utf8");
	return restoreFile;
}

function main() {
	const backupDir = mkdtempSync(resolve(tmpdir(), "glyphin-content-refresh-"));

	try {
		logStep("Regenerating supabase/seed.sql from src/lib/data/thai.ts");
		run(process.execPath, [resolve(scriptDir, "write-thai-seed.mjs")]);

		logStep("Ensuring the local Supabase stack is running");
		run("pnpm", ["exec", "supabase", "start"]);

		const dbUrl = getLocalDbUrl();

		logStep("Backing up local auth and learner state");
		const restoreFile = buildRestoreFile(dbUrl, backupDir);

		logStep("Refreshing local database content from migrations and seed");
		run("pnpm", ["exec", "supabase", "db", "reset", "--yes"]);

		logStep("Ensuring the local Supabase stack is running after the reset");
		run("pnpm", ["exec", "supabase", "start"]);

		logStep("Restoring backed-up auth and learner state");
		run("psql", [dbUrl, "-v", "ON_ERROR_STOP=1", "-f", restoreFile]);

		logStep("Regenerating published lesson artifacts");
		run("pnpm", ["publication:generate"]);

		rmSync(backupDir, { recursive: true, force: true });
		console.log("\nLearner-preserving content refresh completed successfully.");
	} catch (error) {
		console.error(
			`\nLearner-preserving content refresh failed. Backup files were left in ${backupDir} for inspection.`,
		);
		throw error;
	}
}

main();
